def add_ost(word):
    """Добавляет -ость ТОЛЬКО существительным, которые не в исключениях"""
    # Если слово в списке пропуска - не трогаем
    if word.lower() in SKIP_WORDS:
        return word
    
    # Если слово уже муринское - не трогаем
    if word.lower() in MURIN_CORE:
        return word
    
    # Если слово в словаре замен - используем замену
    if word.lower() in WORD_TO_MURIN:
        return WORD_TO_MURIN[word.lower()]
    
    # Если слово в местоимениях - заменяем
    if word.lower() in PRONOUNS:
        return PRONOUNS[word.lower()]
    
    # Если слово с большой буквы (имя) - добавляем ость
    if word and word[0].isupper() and word.lower() not in SKIP_WORDS:
        # Убираем знаки препинания для проверки
        clean = word.strip(".,!?;:()\"'")
        if clean and clean[0].isupper():
            return word + "ость"
    
    # Для остальных слов - только если это существительное (примерно)
    # Проверяем по окончаниям, но очень грубо
    noun_endings = ('а', 'я', 'о', 'е', 'и', 'ы', 'ь', 'й')
    if any(word.lower().endswith(ending) for ending in noun_endings):
        # Но не трогаем короткие слова
        if len(word) > 3:
            return word + "ость"
    
    return word

def translate(text):
    words = text.split()
    result = []
    
    # Выбираем 2-3 слова для возможной замены
    replace_count = min(random.randint(2, 3), len(words))
    replace_indices = random.sample(range(len(words)), replace_count)
    
    for i, word in enumerate(words):
        # Отделяем знаки препинания
        match = re.match(r"^(\W*)(\w+)(\W*)$", word)
        if match:
            prefix, clean, suffix = match.groups()
        else:
            prefix, clean, suffix = "", word, ""
        
        # Если слово выбрано для замены
        if i in replace_indices:
            new_word = add_ost(clean)
        else:
            new_word = clean
        
        # Восстанавливаем регистр
        if clean and clean[0].isupper() and new_word and new_word != clean:
            new_word = new_word[0].upper() + new_word[1:]
        
        result.append(prefix + new_word + suffix)
    
    return ' '.join(result)

